echo "bash_conditionals2.sh"
a=100
b=100
c=200
d=300

echo a=$a b=$b c=$c d=$d                 #display the values

# Conditional tests
if [ $a -eq $b ] ; then 
echo a equals b
fi

if [ $a -ne $b ] ; then 
echo a does not equal b
fi

if [ $a -gt $c ] ; then 
echo a is greater than c
fi

if [ $a -lt $c ] ; then 
echo a is less than c
fi 

if [ $a -ge $d ] ; then 
echo a is greater than or equal to d
fi

if [ $a -le $d ] ; then 
echo a is less than or equal to d
fi 

echo Showing the negation operator:
if [ ! $a -eq $b ] ; then 
echo Clause 1
else 
echo Clause 2
fi 
echo "End of script7"


